  <script>
    const counters = document.querySelectorAll('.counter-inner .item h2 span');
    const speed = 200;

    counters.forEach(counter => {
      const text = counter.textContent.trim();
      const number = parseFloat(text.replace(/[^\d.]/g, ''));
      const suffix = text.replace(/[0-9.]/g, '');
      const increment = number / speed;
      let count = 0;

      const updateCount = () => {
        count += increment;
        if (count < number) {
          counter.textContent = Math.round(count) + suffix;
          requestAnimationFrame(updateCount);
        } else {
          counter.textContent = number + suffix;
        }
      };
      updateCount();
    });
  </script>





