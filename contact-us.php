  <script>
    document.getElementById('contact-main').addEventListener('submit', function(event) {
      event.preventDefault();

      // Clear previous error messages
      document.querySelectorAll('.error-message').forEach(el => el.textContent = '');

      let isValid = true;

      // Get form values
      const name = document.getElementById('name').value.trim();
      const phone = document.getElementById('phone').value.trim();
      const email = document.getElementById('email') ? document.getElementById('email').value.trim() : '';
      const subject = document.getElementById('subject').value;
      const message = document.getElementById('message').value.trim();

      // Validate Name
      if (name === '') {
        document.getElementById('name-error').textContent = 'Name is required';
        isValid = false;
      }

      // Validate Phone (10-15 digits)
      if (phone === '' || !/^\d{10,15}$/.test(phone)) {
        document.getElementById('phone-error').textContent = 'Enter a valid phone number';
        isValid = false;
      }

      // Validate Subject
      if (subject === '' || subject === null) {
        document.getElementById('subject-error').textContent = 'Please select a service';
        isValid = false;
      }

      // Validate Message
      if (message === '') {
        document.getElementById('message-error').textContent = 'Message is required';
        isValid = false;
      }

      if (!isValid) return; // Stop submission if invalid

      // Submit via AJAX
      const formData = new FormData(document.getElementById('contact-main'));

      fetch('mailer.php', {
          method: 'POST',
          body: formData
        })
        .then(response => response.json())
        .then(data => {
          Toastify({
            text: data.message,
            duration: 3000,
            gravity: "top",
            position: "center",
            backgroundColor: data.status === 'success' ? '#28a745' : '#dc3545',
          }).showToast();

          if (data.status === 'success') {
            document.getElementById('contact-main').reset();
          }
        })
        .catch(() => {
          Toastify({
            text: 'Error submitting form. Please try again!',
            duration: 3000,
            gravity: "top",
            position: "center",
            backgroundColor: '#dc3545',
          }).showToast();
        });
    });
  </script>





